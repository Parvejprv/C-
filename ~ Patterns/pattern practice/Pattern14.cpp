/**************************************************  

Pattern No. 14
--------------
n=5
 5 10 15 20 25 
 4  9 14 19 24
 3  8 13 18 23
 2  7 12 17 22
 1  6 11 16 21
 


****************************************************/


#include<iostream>
#include<iomanip>

using namespace std;
int main() {
  int n = 5;

  for(int i=1; i<=n; i++){
    int x = n-i+1;    // 5-1+1  -> 5

    for(int j=1; j<=n; j++){
        cout << setw(2) << x << " ";
        x += n;
    }
    cout  << endl;
  }

  return 0;
}


